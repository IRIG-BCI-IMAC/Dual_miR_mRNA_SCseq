## Functions for grid table display parameters
modify_cells_colors <- function(g, ids, coloris){
  for (x in 1:length(ids)){
    g$grobs[ids[x]][[1]][["gp"]] <- gpar(col=coloris[x],fontsize=12)
  }
  return(g)
}

modify_cells <- function(g, ids, gp=gpar()){
  for(id in ids) g$grobs[id][[1]][["gp"]] <- gp
  return(g)
}

