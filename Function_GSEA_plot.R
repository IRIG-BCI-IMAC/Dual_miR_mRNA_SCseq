                          xmin = quantile(p.res$data$x, .05),
                          xmax = quantile(p.res$data$x, .3),
                          ymin = quantile(p.res$data$runningScore, .4),
                          ymax = quantile(p.res$data$runningScore, .4))
    }else{
    
