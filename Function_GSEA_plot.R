    # recuperation des vecteurs avec les resultats
    vec_Thr <- c()
    for (i in x@result[["ID"]]){
      separation <- strsplit(i,'_')
      ID <-separation[[1]][2]
      vec_Thr <- c(vec_Thr,ID)
    }
    
    `Nb targets`<-c()
    for (i in x@geneSets){
      nb_targets <- length(which(i %in% names(x@geneList)))
      `Nb targets`<-c(`Nb targets`,nb_targets)
    }
    
    order_res <- order(-(as.numeric(vec_Thr)))
    
    `ES`<- signif(x@result[["enrichmentScore"]],digits=2)[order_res]
    `p-value`<- signif(x@result[["pvalue"]],digits=2)[order_res]
    `Thr` <- vec_Thr[order_res]
    
    my_table <- tableGrob(as.matrix(t(rbind(`Thr`,`Nb targets`,`ES`,`p-value`))))
    
    # rows coloration 
    
    
    max=(len_geneSetID +2)*4
    min=(2*4+1)
    vec_color <- rep(color,4)
    vec_inds <- c(min:max)
    
    my_table<- modify_cells(my_table, c(1:4), gpar(fontsize=9))
    my_table<- modify_cells_colors(my_table, vec_inds, vec_color)
    
    
    if(sum(`ES`)<0){
      p.res <- p.res+ theme(legend.position = "none") +
